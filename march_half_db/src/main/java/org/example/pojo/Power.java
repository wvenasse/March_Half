package org.example.pojo;

public class Power {
    private  Integer powerId;
    private  String powerIdentity;
    private  String powerMy;
    private  String powerOrder;
    private  String powerFavor;
    private  String powerPost;

    public Integer getPowerId() {
        return powerId;
    }

    public void setPowerId(Integer powerId) {
        this.powerId = powerId;
    }

    public String getPowerIdentity() {
        return powerIdentity;
    }

    public void setPowerIdentity(String powerIdentity) {
        this.powerIdentity = powerIdentity;
    }

    public String getPowerMy() {
        return powerMy;
    }

    public void setPowerMy(String powerMy) {
        this.powerMy = powerMy;
    }

    public String getPowerOrder() {
        return powerOrder;
    }

    public void setPowerOrder(String powerOrder) {
        this.powerOrder = powerOrder;
    }

    public String getPowerFavor() {
        return powerFavor;
    }

    public void setPowerFavor(String powerFavor) {
        this.powerFavor = powerFavor;
    }

    public String getPowerPost() {
        return powerPost;
    }

    public void setPowerPost(String powerPost) {
        this.powerPost = powerPost;
    }
}
